<p><h2>§1</h2>
<p align="left">Ilekroć w treści niniejszego Regulaminu lub w umowach o korzystanie z internetowego systemu
rezerwacyjnego euroTICKET On-Line pojawiają się następujące słowa i oznaczenia, należy rozumieć je
jak podano poniżej:</br>
1. „Operator” oznacza firmę ITSOFT spółka z ograniczoną odpowiedzialnością spółka komandytowa.</br>
2. „System” oznacza system euroTICKET On-Line, którego właścicielem i administratorem jest
Operator.</br>
3. „Moduł” oznacza wydzieloną część Systemu dotyczącą jednego typu usług Oferenta.</br>
4. „Moduł „Autokary”” oznacza Moduł dotyczący sprzedaży biletów komunikacji autokarowej przez
Użytkownika (z wyjątkiem stron WWW).</br>
5. „Moduł „Ubezpieczenia”” oznacza Moduł dotyczący sprzedaży polis turystycznych przez Użytkownika
(z wyjątkiem stron WWW).</br>
6. „Moduł „WWW”” oznacza Moduł dotyczący sprzedaży usług Oferenta przez strony WWW.</br>
7. „Moduł „Karty”” oznacza Moduł dotyczący sprzedaży kart przez Użytkownika (z wyjątkiem stron
WWW).</br>
8. „Użytkownik” oznacza firmę, która na podstawie umowy podpisanej z Operatorem ma prawo do
korzystania z usług Systemu.</br>
9. „Oferent” oznacza firmę, która na mocy umowy zawartej z Operatorem sprzedaje swoje usługi za
pośrednictwem Systemu. Oferent jest jednocześnie Użytkownikiem.</br>
10. ”Agent” oznacza firmę, która pośredniczy w sprzedaży usług za pomocą Systemu, na podstawie
umów zawartych z Oferentami. Agent jest jednocześnie Użytkownikiem.</br>
11.”Oddział” oznacza konto w Systemie, którego właścicielem jest Agent, przeznaczone do sprzedaży
usług poprzez System w innej lokalizacji niż konto Agenta. Oddział jest jednocześnie Użytkownikiem.
12. „Umowa” oznacza umowę o świadczenie usług zawartą między Operatorem, a Użytkownikiem.</br>
13. „Usługa” oznacza usługi świadczone przez Operatora na podstawie Umowy.</br>
14. „Faktura” oznacza dokument księgowy będący podstawą dokonywania przez Użytkownika płatności
za Usługę świadczoną na jego rzecz przez Operatora.</br>
15. „Okres Rozliczeniowy” oznacza okres jednego miesiąca, liczony od 11-go danego miesiąca do 10-
go następnego miesiąca, za który dokonywane są rozliczenia należności Agenta wobec Operatora
z tytułu świadczonych Usług, wskazanych przez Operatora na Fakturze.</br>
16. „Oprogramowanie Użytkownika” oznacza oprogramowanie dostarczone przez Operatora
i zainstalowane na komputerze Użytkownika.</br>
17. ”Trasa” oznacza połączenie z grupy miast w jednym kraju do grupy miast w tym samym lub innym
kraju realizowane jednym autokarem, przy czym połączenia antenowe na terenie kraju Oferenta nie są
traktowane jako osobna trasa.</br>
<h2>§2</h2>
<p align="left">Operator ustala w niniejszym Regulaminie zasady świadczenia usług związanych z udostępnieniem
Systemu oraz zasady korzystania z Systemu.</br></p>
<h2>§3</h2>
<p align="left">Operator zapewnia korzystanie z Systemu pod warunkiem posiadania przez Użytkownika: komputera
z zainstalowanym systemem operacyjnym z rodziny Windows (XP / VISTA / 7), Oprogramowania
Użytkownika, dostępu do sieci INTERNET, a za jej pośrednictwem do serwera Systemu, oraz po
spełnieniu innych wymagań technicznych koniecznych do prawidłowej pracy Systemu.</br></p>
<h2>§4</h2>
<p align="left">Operator świadczy usługi związane z Systemem w zakresie i na zasadach określonych w Umowie.
